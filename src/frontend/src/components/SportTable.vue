              </v-container>
            </v-card-text>

            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn color="blue darken-1" text @click="close">
                Cancel
              </v-btn>
              <v-btn color="blue darken-1" text @click="save">
                Save
              </v-btn>
            </v-card-actions>
          </v-card>
        </v-dialog>

        <v-dialog v-model="dialogDelete" max-width="500px">
          <v-card>
            <v-card-title class="headline">Are you sure you want to delete this item?</v-card-title>
            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn color="blue darken-1" text @click="closeDelete">Cancel</v-btn>
              <v-btn color="blue darken-1" text @click="deleteSport">OK</v-btn>
              <v-spacer></v-spacer>
            </v-card-actions>
          </v-card>
        </v-dialog>
      </v-toolbar>
    </template>
    <template v-slot:[`item.actions`]="{ item }">
      <v-icon small class="mr-2" @click="editModal(item)">
        mdi-pencil
      </v-icon>
      <v-icon small @click="deleteModal(item)">
        mdi-delete
      </v-icon>

    </template>
  </v-data-table>
</template>

<script>
import http from '@/http'

export default {
	name : 'SportTable',
    props: ['theData', 'theHeader'],
	data() {
		return{
            dialog : false,
            dialogDelete : false,
            active_id : -1,
            editedItem: {
                id_sport : '',
                name: ''
            },
            defaultItem: {
                id_sport : '',
                name : ''
            }
		}
	},

    computed : {
        formTitle(){
            return this.active_id === -1? 'Add Sport' : 'Edit Sport'
        }
    },

	methods : {
    close : function(){
        this.dialog = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.active_id = -1
        })
    },
    save () {
        if (this.active_id > -1) {
            //Edit
            http.put(`/sports/${this.active_id}`, {
			        'name' : this.editedItem.name
            })
            
            //send event to google analytics
            this.$gtag.event('edit_sport', {
              'event_category' : 'Admin',
              'event_label' : 'Edit existing sport'
            })
        } 
        else {
            //CREATE
            try{
			        http.post('/sports/', {
				        'name' : this.editedItem.name
              })
              
              //send event to google analytics
                this.$gtag.event('add_sport', {
                  'event_category' : 'Admin',
                  'event_label' : 'Add new sport'
                })
		        }
		        catch(err){
			        console.log(err)
		        }
        }
        this.close()
        window.location.reload()
    },

	editModal : function(sport){
        this.editedItem = Object.assign({}, sport)
        this.active_id = sport['id_sport']
        this.dialog = true
	},

    addModal : function(){
        console.log("got here")
		this.$jquery('#addSportModal').modal('toggle');
    },
    
	deleteModal : function(sport){
		this.active_id = sport['id_sport']
		this.dialogDelete = true
	},

    closeDelete () {
        this.dialogDelete = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.active_id = -1
        })
    },

	deleteSport : function(){
        http.delete(`sports/${this.active_id}`)
        this.dialogDelete = false
        window.location.reload()

        //send event to google analytics
        this.$gtag.event('delete_sport', {
            'event_category' : 'Admin',
            'event_label' : 'Delete existing sport'
        })
	}
}

};



</script>

